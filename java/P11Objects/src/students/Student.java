package students;

public class Student {
	public String id;
	public String name;
	
	public Student() {}
	public Student(String id, String name) {
		this.id = id;
		this.name = name;
	}
	
	@Override
	public String toString() {
		String s = super.toString();
		System.out.printf("[Student] (%s): id=%s, name =%s%n", s, this.id, this.name);
		return s;
	}
}
