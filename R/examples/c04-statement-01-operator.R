# 연산자: # 산술연산자
# 사칙연산 : +, -, *, /
# 나머지: %%
# 제곱: ^, **

# 나머지
x <- 10
y <- 3
z <- x %% y
cat('나머지는?', z)

# 제곱
x3 <- x ^ 3
x3

y3 <- y ** 3
y3

# 관계연산자: ==, !=, >, >=, <, <=
b1 <- x > y
b1
b2 <- x != y
b2
b3 <- x < y
b3
b4 <- x >= y
b4
b5 <- x <= y
b5
b6 <- x == y
b6

# 논리연산자: &, |, !, xor()
l1 <- x > y & x <= 10
l1

l2 <- x < y | z >= 1
l2

l3 <- !l2
l3

l4 <- !(x < y | z >= 1)
l4
l5 <- !FALSE
l5
